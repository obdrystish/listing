package ru.vshp.listing2;

public class AlphabetDoWhileDemo {
    public class AlphabetDoWhileDemo {
        public static void main(String[] args) {
            char ch = 'Я';
            do {
                System.out.println(ch);
                ch--;
            } while (ch >= 'А');
        } // main (String[])
    } // AlphabetDoWhileDemo class
}
